<section class="wiki-header">
    <h1>📦 Posting Listings</h1>
    <p>Guidance for creating effective listings so buyers find and trust your posts.</p>
</section>

<section class="wiki-content-section">
    <h2>Before you create a listing</h2>
    <div class="wiki-card">
        <h3>Decide what to include</h3>
        <p>Good listings have: a clear title (book name + edition + course code if known), the condition, asking price, and several photos. Mention any extras like solutions manuals or associated notes.</p>
    </div>

    <h2>Writing the listing</h2>
    <div class="wiki-card">
        <h3>Title & description</h3>
        <p>Title example: "Calculus (8th ed) — MATH101 — Good condition". In the description, include edition, any markings/highlights, reason for selling, and whether you accept pickup only.</p>
    </div>

    <div class="wiki-card">
        <h3>Pricing guidance</h3>
        <p>Search similar listings to set a competitive price. Typical reductions are 30–70% off retail depending on condition. Indicate if price is firm or negotiable.</p>
    </div>

    <h2>Photos</h2>
    <div class="wiki-card">
        <h3>Photo tips</h3>
        <ul>
            <li>Use natural light and a neutral background; avoid cluttered photos.</li>
            <li>Crop or rotate images so covers are centered and readable.</li>
        </ul>
    </div>

    <h2>After posting</h2>
    <div class="wiki-card">
        <h3>Managing offers</h3>
        <p>Respond quickly to interested buyers, update your listing when the item is reserved or sold, and mark it sold when transaction completes so others don't message you about it.</p>
    </div>

    <h2>Checklist before publish</h2>
    <div class="wiki-card">
        <ul>
            <li>Is the title clear and searchable?</li>
            <li>Are the photos clear and showing condition?</li>
            <li>Have you set a reasonable price?</li>
            <li>Did you choose the correct format and category?</li>
        </ul>
    </div>
</section>
@endsection
