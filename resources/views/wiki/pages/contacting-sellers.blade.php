<section class="wiki-header">
    <h1>💬 Contacting Sellers</h1>
    <p>How to communicate safely and effectively with buyers and sellers on CampusShelf.</p>
</section>

<section class="wiki-content-section">
    <h2>Initial message</h2>
    <div class="wiki-card">
        <h3>What to include</h3>
        <ul>
            <li>Introduce yourself briefly (e.g. "Hi, I'm a student in MATH101").</li>
            <li>Reference the listing title or ID so the seller knows which item you mean.</li>
            <li>Ask any clarifying questions about condition, included material, or pickup preferences.</li>
            <li>Suggest availability windows for pickup if you are ready to buy.</li>
        </ul>
    </div>

    <h2>Negotiation etiquette</h2>
    <div class="wiki-card">
        <h3>Polite offers</h3>
        <p>Politely state your offer and whether it is cash or electronic payment. Respect the seller's stated price if they mark it as "firm".</p>
    </div>

    <h2>Arranging pickup</h2>
    <div class="wiki-card">
        <h3>Meeting locations</h3>
        <p>Choose a safe, public on-campus spot (library entrance, student centre, coffee shop). Confirm date/time and bring correct change if paying cash.</p>
    </div>

    <div class="wiki-card">
        <h3>Timing and confirmation</h3>
        <p>If you reserve an item, confirm the pickup window and ask the seller to mark it reserved so others know it's taken. If you can't make it, inform the seller as soon as possible.</p>
    </div>

    <h2>After the transaction</h2>
    <div class="wiki-card">
        <h3>Mark as sold and leave notes</h3>
        <p>Once complete, the seller should update or remove the listing. Consider leaving a polite follow-up message confirming receipt.</p>
    </div>

    <h2>Privacy & safety</h2>
    <div class="wiki-card">
        <h3>Protect personal info</h3>
        <p>Don't share personal addresses or payment information via public fields. Use private email or meet in person to exchange details.</p>
    </div>

    <h2>Reporting issues</h2>
    <div class="wiki-card">
        <h3>Fraudulent or suspicious behaviour</h3>
        <p>If a buyer or seller behaves inappropriately or a listing appears fraudulent, contact site administrators and provide message screenshots and listing links so they can investigate.</p>
    </div>
</section>
@endsection

